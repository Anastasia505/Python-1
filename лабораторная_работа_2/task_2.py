# Исходные данные
money_capital = 20000  # Подушка безопасности
salary = 5000  # Ежемесячная зарплата
spend = 6000  # Траты за первый месяц
increase = 0.05  # Ежемесячный рост цен

# Переменная для подсчета месяцев
months = 0

# Цикл для расчета месяцев, изменили условие выхода из цикла 
while money_capital >= 0:
    # Уменьшаем подушку безопасности на недостающую сумму
    money_capital -= max(spend - salary, 0)  # Если зарплата покрывает расходы, деньги не тратятся

    # Проверяем, хватило ли средств на текущий месяц
    if money_capital < 0:
        break

    # Увеличиваем расходы на 5% для следующего месяца
    spend *= (1 + increase)

    # Увеличиваем счетчик месяцев
    months += 1

# Вывод результата
print("Количество месяцев, которое можно протянуть без долгов:", months)
