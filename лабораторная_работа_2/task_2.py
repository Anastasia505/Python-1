# Исходные данные
money_capital = 20000  # Подушка безопасности
salary = 5000  # Ежемесячная зарплата
spend = 6000  # Траты за первый месяц
increase = 0.05  # Ежемесячный рост цен

# Переменная для подсчета месяцев
months = 0

# Цикл для расчета месяцев
while money_capital >= 0:
    # Бюджет текущего месяца (подушка безопасности + зарплата)
    budget = money_capital + salary

    # Проверяем, хватает ли бюджета на текущие расходы
    if budget < spend:
        break  # Если не хватает, выходим из цикла

    # Уменьшаем подушку безопасности на недостающую сумму
    money_capital -= (spend - salary)

    # Увеличиваем расходы на 5% для следующего месяца
    spend *= (1 + increase)

    # Увеличиваем счетчик месяцев
    months += 1

# Вывод результата
print("Количество месяцев, которое можно протянуть без долгов:", months)
